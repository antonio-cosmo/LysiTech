const expandir = document.querySelector('.expandir-icon-container');
const expandindo = document.querySelector('.expandir-icon');
const mural = document.querySelector('.mural');
const divisoria = document.querySelector('.divisoria');

expandir.addEventListener('click', () => {
    mural.classList.toggle('expandindo');
    expandindo.classList.toggle('active');
    divisoria.classList.toggle('active');
});

